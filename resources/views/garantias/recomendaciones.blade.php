<h4>Instructivo para el Punto de Venta de verificación de productos Piero:</h4>
Siendo que Piero se reserva el derecho, como condición de garantía (legal o GEP), de verificar por si o a través de terceros, la o las fallas alegadas como causa del reclamo y atento que la negativa del consumidor o la imposibilidad de la verificación del producto por causas ajenas a Piero, podrá ser considerada como una causal de exclusión de la garantía (legal o GEP), recomendamos que v/ parte al intervenir y coordinar en las gestiones de los consumidores tendientes a la ejecución de la garantía legal o GEP adopte ciertas medidas a los fines de un mejor análisis y tratamientos de los reclamos por productos fabricados por Piero.</br>
La verificación de productos denunciados como defectuosos por los consumidores estará a su cargo, riesgo y costo .</br>
En todos los casos recomendamos que:</br>
• la fijación de la fecha, lugar y horario de la misma sea previamente convenida por escrito con el consumidor en cuestión;</br>
• de la verificación del producto se labre –por escrito- un documento de verificación el cual deberá ser suscripto por la persona a cargo de la verificación del mismo (en adelante, el “Reporte de Verificación”) y por el consumidor o la persona que este expresamente autorice para intervenir en esta diligencia; </br>
• como elementos esenciales y necesarios el Reporte de Verificación contenga todos los datos del cliente como ser: nombre y apellido completo, domicilio, correo electrónico y teléfonos de contacto; lugar, fecha y hora de la diligencia de verificación, producto involucrado, y los datos de la persona que facilita tal diligencia que podría ser el consumidor o persona debidamente autorizada por aquel;</br>
• en el Reporte de verificación el emisor constate: </br>
- Causas del reclamo que motivaron la inspección;</br>
- Estado actual del producto; </br>
- Causa/s denunciada/s por el consumidor;</br>
- Otras causas que podrían estar afectando al producto; </br>
- Presencia de causas que excluyan la validez de la garantía; </br>
- Información adicional suministrada por el consumidor respecto al uso del producto; </br>y
- Cualquier otra información o dato que fuera de interés o relevante para determinar el estado del producto al momento de la verificación. </br>
En todos los casos el Reporte de Verificación deberá ser puesto a disposición de Piero junto a todos los elementos y material recabado al momento de dicha diligencia como ser fotografías (facilitadas por el consumidor o propias, croquis, etc.</br>
